class CompoundRegex:
    def __init__(self, raw_pattern_list):
        self.raw_pattern_list = []
        self.raw_not_like_tuple_list = []

        for raw_pattern in raw_pattern_list:
            if isinstance(raw_pattern, str):
                self.raw_pattern_list.append(raw_pattern)
            elif isinstance(raw_pattern, tuple):
                self.raw_not_like_tuple_list.append(raw_pattern)
            else:
                raise ValueError(
                    "Invalid type of raw_pattern :", type(raw_pattern))

        self.or_pattern = self.make_or_pattern(self.raw_pattern_list)

    def make_or_pattern(self, raw_or_pattern_list):
        or_pattern_list = []
        if len(raw_or_pattern_list) != 0:
            for raw_or_pattern in raw_or_pattern_list:
                if isinstance(raw_or_pattern, str):
                    pattern = raw_or_pattern
                    if not raw_or_pattern.startswith("%"):
                        pattern = "^" + pattern
                    if not raw_or_pattern.endswith("%"):
                        pattern += "$"
                    pattern = re.sub(r"(^%|%$)", "", pattern)
                    or_pattern_list.append(pattern)

            or_pattern = "|".join(or_pattern_list)
            return re.compile(f"({or_pattern})")
        else:
            return re.compile(f"(^$)")

    def remove(self, word):
        if re.search(self.or_pattern, word):
            return True
        else:
            # cleaned_word = re.sub(self.or_pattern, "", word)
            for raw_pattern in self.raw_not_like_tuple_list:
                like_conditions, not_like_conditions = raw_pattern
                if isinstance(like_conditions, str):
                    like_conditions = (like_conditions,)
                if isinstance(not_like_conditions, str):
                    not_like_conditions = (not_like_conditions,)
                or_pattern = self.make_or_pattern(like_conditions)
                not_like_pattern = self.make_or_pattern(not_like_conditions)
                if re.search(not_like_pattern, word):
                    return False
                elif re.search(or_pattern, word):
                    return True
                    # for not_like_word in not_like_conditions:
                    #     if not_like_word in word:
                    #         return word
                    # cleaned_word = re.sub(or_pattern, "", cleaned_word)
                else:
                    return False
            return False


